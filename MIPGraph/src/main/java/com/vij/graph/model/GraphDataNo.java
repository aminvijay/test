package com.vij.graph.model;

public class GraphDataNo {

	private int count;
	private double avgofPROCESSINGTIME;
	private double avgofOVERALLTIME;
	private double avgofRECORD_COUNT;
	private int hours;
	
	
	public int getHours() {
		return hours;
	}
	public void setHours(int hours) {
		this.hours = hours;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public double getAvgofPROCESSINGTIME() {
		return avgofPROCESSINGTIME;
	}
	public void setAvgofPROCESSINGTIME(double avgofPROCESSINGTIME) {
		this.avgofPROCESSINGTIME = avgofPROCESSINGTIME;
	}
	public double getAvgofOVERALLTIME() {
		return avgofOVERALLTIME;
	}
	public void setAvgofOVERALLTIME(double avgofOVERALLTIME) {
		this.avgofOVERALLTIME = avgofOVERALLTIME;
	}
	public double getAvgofRECORD_COUNT() {
		return avgofRECORD_COUNT;
	}
	public void setAvgofRECORD_COUNT(double avgofRECORD_COUNT) {
		this.avgofRECORD_COUNT = avgofRECORD_COUNT;
	}
	
}
